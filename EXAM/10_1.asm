DATAS SEGMENT
	INFO1 DB 'INPUT HEX(CAPITAL LETTER):$'
	INPUT DB 5,6 DUP(0)
		DB '$'
	NUMBER DW 0
	MUL_NUM DW 16
	CR_LF DB 0DH,0AH,'$'
DATAS ENDS

CODE SEGMENT
	ASSUME DS:DATAS,CS:CODE
START:
	MOV AX,DATAS
	MOV DS,AX
	
	;DISPLAY
	LEA DX,INFO1
	MOV AH,9
	INT 21H
	
	;INPUT
	LEA DX,INPUT
	MOV AH,0AH
	INT 21H
	
	LEA SI,INPUT[2]
	MOV CX,0
	MOV CL,INPUT[1]
	MOV AX,0
INP:
	MOV DL,[SI]
	CMP DL,'9'
	JBE NUM
LETER:
	SUB DL,55
	JMP GO
NUM:
	SUB DL,30H	
GO:	
	MOV DH,0
	PUSH DX
	MOV AX,NUMBER
	MUL MUL_NUM
	POP DX
	ADD AX,DX
	MOV NUMBER,AX
	INC SI
	LOOP INP
	
	
	LEA DX,CR_LF
	MOV AH,9
	INT 21H
	
	;DISP
	MOV DX,0
	MOV AX,NUMBER
	MOV CX,10000
	DIV CX
	MOV BX,DX
	MOV DL,AL
	ADD DL,30H
	MOV AH,2
	INT 21H
	
	MOV DX,0
	MOV AX,BX
	MOV CX,1000
	DIV CX
	MOV BX,DX
	MOV DL,AL
	ADD DL,30H
	MOV AH,2
	INT 21H
	
	MOV DX,0
	MOV AX,BX
	MOV CX,100
	DIV CX
	MOV BX,DX
	MOV DL,AL
	ADD DL,30H
	MOV AH,2
	INT 21H
	
	MOV DX,0
	MOV AX,BX
	MOV CX,10
	DIV CX
	MOV BX,DX
	MOV DL,AL
	ADD DL,30H
	MOV AH,2
	INT 21H
	
	MOV DX,0
	MOV AX,BX
	MOV CX,1
	DIV CX
	MOV BX,DX
	MOV DL,AL
	ADD DL,30H
	MOV AH,2
	INT 21H
		
	MOV AH,4CH
	INT 21H
CODE ENDS
END START
